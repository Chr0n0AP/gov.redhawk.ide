package gov.redhawk.ide.graphiti.dcd.ui;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class DCDUIGraphitiPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "gov.redhawk.ide.graphiti.dcd.ui"; //$NON-NLS-1$

	private static DCDUIGraphitiPlugin plugin;

	public DCDUIGraphitiPlugin() {
	}

	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	public static DCDUIGraphitiPlugin getDefault() {
		return plugin;
	}

}
